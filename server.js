const express = require('express');
const cors = require('cors');
require('dotenv').config();

const app = express();
const PORT = process.env.PORT || 3000;

// Middleware
app.use(cors());
app.use(express.json());
app.use(express.urlencoded({ extended: true }));

// Routes
app.get('/', (req, res) => {
  res.json({
    message: 'Blockchain Backend Server is running!',
    hardhat: 'Ready for smart contract development',
    network: 'localhost'
  });
});

// Blockchain/Contract routes
app.get('/api/blockchain/status', (req, res) => {
  res.json({
    status: 'connected',
    network: 'hardhat',
    message: 'Local blockchain network is ready'
  });
});

// Health check endpoint
app.get('/health', (req, res) => {
  res.status(200).json({ status: 'OK', timestamp: new Date().toISOString() });
});

// Error handling middleware
app.use((err, req, res, next) => {
  console.error(err.stack);
  res.status(500).json({ error: 'Something went wrong!' });
});

// 404 handler
app.use('*', (req, res) => {
  res.status(404).json({ error: 'Route not found' });
});

app.listen(PORT, () => {
  console.log(`ğŸš€ Server running on http://localhost:${PORT}`);
  console.log(`â›“ï¸  Blockchain backend ready for Hardhat integration`);
});

module.exports = app;